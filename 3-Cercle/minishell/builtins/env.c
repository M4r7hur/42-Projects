#include "minishell.h"

void	builtin_env(char **env)
{
	int	i;

	i = 0;
	while (env[i] != NULL)
	{
		ft_putstr_fd(env[i], 0);
		ft_putchar_fd('\n', 0);
		i++;
	}
}
